// Scoreboard
//
// A class that defines how a scoreboard acts
// Includes displaying the player score

// Scoreboard constructor
//
// Sets the properties with the provided arguments
function Scoreboard (x,y,width,height,fill,name){
  this.x = x;
  this.y = y;
  this.width = width;
  this.height = height;
  this.fill = fill;
  this.name = name;
}
